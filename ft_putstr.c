#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	if (!str)
		return (write(1, "(null)", 6));
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
		j++;
	}
	return (j);
}
