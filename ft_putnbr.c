#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int				j;
	char			c;

	j = 0;
	if (n == -2147483648)
		return (write(1, "-2147483648", 11));
	if (n < 0)
	{
		n = n * -1;
		j += ft_putchar('-');
	}
	if (n > 9)
	{
		j += ft_putnbr(n / 10);
		j += ft_putnbr(n % 10);
	}
	if (n < 10)
	{
		c = n + 48;
		j += ft_putchar(c);
	}
	return (j);
}
